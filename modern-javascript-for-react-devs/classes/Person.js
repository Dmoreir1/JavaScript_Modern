export default class Person {

  ///////////////////////////////////////////////////////////////////////////
  // Data members  (fields or properties)
  ///////////////////////////////////////////////////////////////////////////
  
  name = "";
  #cat = null;

  ///////////////////////////////////////////////////////////////////////////
  // Constructors
  ///////////////////////////////////////////////////////////////////////////

  constructor(name) {
    this.name = name;
  }

  ///////////////////////////////////////////////////////////////////////////
  // Public Methods
  ///////////////////////////////////////////////////////////////////////////

  introduceSelf() {
    console.log(`Hi! My name is ${this.name}.`);
  }

  setCat(cat) {
    this.#cat = cat;
  }

  getCatName() {
    return this.#cat.name;
  }
}
